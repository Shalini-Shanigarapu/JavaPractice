package com.learn.javaBasics;

public class PrePost {
    public static void main(String[] args){
        int a=1,b=2;
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b--);
        ++a;//2
        System.out.println(a--);//2
        System.out.println(a);//1
        System.out.println(a++);//1
        System.out.println(a);//2
        System.out.println(--a);//1






    }
}
